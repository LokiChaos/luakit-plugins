-- Returns the host's domain.effective.tld there is no regex way to do this
-- We check against a table of "public suffixes" from Mozilla
-- returns entire host string if given an IP (ipv4) or a
getdomain = function (host)
	if host then
		if string.match(host, "^[0-9%.]-$") then
			-- the host is an ipv4 address
			return host
		-- TODO add ipv6
		else
			local bits = util.table.reverse(util.string.split(host, "%.") or {})

			-- Get domain from public suffix list
			local phost = tld
			local n = 1
			repeat
				-- Wildcard match
				if phost["*"] and bits[n] and not phost["!" .. bits[n]] then
					phost = nil
					n = n + 1
				else
					phost = phost[bits[n]]
				end
				n = n + 1
			until not phost

			-- Build the domain.effectiveTLD and return that
			local rethost = bits[1]
			local m = 2
			repeat
				rethost = (bits[m] or "") .. "." .. rethost
				m = m + 1
			until m >= n
			return rethost
		end
	end
end


